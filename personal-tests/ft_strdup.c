#include "push_swap.h"

char	*ft_strdup(const char *s)
{
	int		strlen;
	int		i;
	char	*ptr;

	i = 0;
	strlen = ft_strlen(s);
	ptr = malloc((sizeof(char) * strlen + 1));
	if (ptr == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
